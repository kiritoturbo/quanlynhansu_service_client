    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (JOptionPane.showConfirmDialog(this, "bạn có muốn thêm không?") == JOptionPane.NO_OPTION) {
            return;
        }
        String MaNhanVien = txtMaNhanVien.getText();
        String tenNhanVien = txtTenNhanVien.getText();
        String PhongBan = cboPhongBan.getSelectedItem().toString();
        String ChucVu = cboChucVu.getSelectedItem().toString();
        String ChuyenMon = cboChuyenMon.getSelectedItem().toString();
        String TrinhDoHocVan = cboTrinhDoHocVAn.getSelectedItem().toString();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        Date BirthDay = date_Ngaysinh.getDate();
        String ngaysinh = formatter.format(BirthDay);
        XMLGregorianCalendar xmlDate = null;
        // Gregorian Calendar object creation
        GregorianCalendar gc = new GregorianCalendar();
        // giving current date and time to gc
        gc.setTime(BirthDay);
        try {
            xmlDate = DatatypeFactory.newInstance()
                    .newXMLGregorianCalendar(gc);
        } catch (Exception e) {
            e.printStackTrace();
        }
        String a = null;
        if (rdbNam.isSelected()) {
            a = "nam";
        } else if (rdbnu.isSelected()) {
            a = "nữ";
        }

        String diaChi = txtDiaChi.getText();
        String SoDienThoai = txtSDT.getText();
        String email = txtEmail.getText();
        Nhanvien std = new Nhanvien();
        JOptionPane.showMessageDialog(this, quanlynhansuModifly.addNhanvien(MaNhanVien, tenNhanVien, PhongBan, ChucVu, TrinhDoHocVan, ChuyenMon, xmlDate, a, diaChi, SoDienThoai, email));
        showAllNhanVien();
        txtMaNhanVien.setText("");
        txtTenNhanVien.setText("");
        txtDiaChi.setText("");
        txtEmail.setText("");
        txtSDT.setText("");
    }//GEN-LAST:event_btnThemActionPerformed

