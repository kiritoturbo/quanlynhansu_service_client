    private void showAllNhanVien() {
        tableModel.setRowCount(0);
        List<Nhanvien> list = quanlynhansuModifly.findAllNhanvien();
        for (Nhanvien item : list) {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1,
                item.getMaNv(), item.getTenNv(), item.getPhongban(), item.getChucvu(), item.getTdHocvan(), item.getChuyenmon(),
                item.getNgaysinh(), item.getGioitinh(), item.getDiachi(), item.getEmail(), item.getSdt()

            });
        }
    }

    private void comboboxInit() {
        cboPhongBan.removeAllItems();
        cboChucVu.removeAllItems();
        cboTrinhDoHocVAn.removeAllItems();
        cboChuyenMon.removeAllItems();

        tableModel.setRowCount(0);
        List<Phongban> list = quanlynhansuModifly.findAllPhongban();
        for (Phongban item : list) {
            cboPhongBan.addItem(item.getTenPb());
        }
        List<Chucvu> list1 = quanlynhansuModifly.findAllChucvu();
        for (Chucvu item : list1) {
            cboChucVu.addItem(item.getTenCv());
        }
        List<Trinhdohocvan> list2 = quanlynhansuModifly.findAllTrinhdohocvan();
        for (Trinhdohocvan item : list2) {
            cboTrinhDoHocVAn.addItem(item.getTenTdhv());
        }
        List<Chuyenmon> list3 = quanlynhansuModifly.findAllChuyenmon();
        for (Chuyenmon item : list3) {
            cboChuyenMon.addItem(item.getTenCm());
        }
    }
